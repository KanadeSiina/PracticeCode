void pkas()
{
    static int kas=0;
    cout<<"Case #"<<++kas<<": ";
}
int main()
{
    int T;
    cin>>T;
    while(T--)
    {
        ll a,b;
        cin>>a>>b;
        bool f=0;
        if(b>a) f=1,swap(a,b);
        ll d=a-b;
        ll L=0,R=2e9,ans=-1;
        while(L<=R)
        {
            ll mid=L+R>>1;
            ll cnt=(1+mid)*mid/2;
            if(cnt>d)
                R=mid-1;
            else L=mid+1,ans=mid;
        }
        assert(ans!=-1);
        a-=(1+ans)*ans/2;
        if(a==b) f=0;
        L=0,R=2e9;ll ans2=-1;
        //dbg(a,b);
        ll cca,ccb;
        while(L<=R)
        {
            ll mid=L+R>>1;
            ll ca=(mid+1)/2,cb=mid-ca;
            ll cnta=(ans+1+ans+1+(ca-1)*2)*ca/2,cntb=(ans+2+ans+2+(cb-1)*2)*cb/2;
            if(cnta<=a&&cntb<=b)
            {
                ans2=mid;
                L=mid+1;
                cca=cnta,ccb=cntb;
            }
            else R=mid-1;
        }
        //dbg(cca,ccb);
        a-=cca,b-=ccb;
        assert(ans2!=-1);
        if(f) swap(a,b);
        ans+=ans2;
        pkas();
        cout<<ans<<" "<<a<<" "<<b<<'\n';
    }
}