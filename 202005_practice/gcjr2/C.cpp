#define x first
#define y second
vc<pi> p;
int n;
int ans;
int cur=0;
vi nxt;
vi vis;
inline bool judge(pi a,pi b)
{
    return (ll)a.y*b.x==(ll)a.x*b.y;
}
inline ll dis(pi a,pi b)
{
    int dx=a.x-b.x;
    int dy=a.y-b.y;
    return (ll)dx*dx+(ll)dy*dy;
}
void dd(int st,int u,pi d)
{
    if(!vis[u]) cur++;
    u=nxt[u];
    if(u==-1) return;
    if(vis[u]) return;
    cur++;
    vis[u]=1;
    vi tar;
    for(int j=0;j<n;j++)
    {
        pi cd(p[j].x-p[u].x,p[j].y-p[u].y);
        if(judge(cd,d)) tar.push_back(j);
    }
    sort(tar.begin(),tar.end(),[&](int i,int j){
        return dis(p[i],p[u])<dis(p[j],p[u]);
    });
    if(tar.size())
        dd(st|(1<<tar[0]),tar[0],d);
}
void upd(vc<pi>& a)
{
    nxt=vi(n,-1);
    for(auto u:a)
    {
        nxt[u.x]=u.y;
        nxt[u.y]=u.x;
    }
    //dbg(nxt);
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<n;j++)
        {
            if(i==j) continue;
            vis=vi(n);
            vis[i]=vis[j]=1;
            pi d(p[j].x-p[i].x,p[j].y-p[i].y);
            cur=2;
            dd((1<<i)|(1<<j),j,d);
        }
    }
    ans=max(ans,cur);
}
void dfs(int st,vc<pi> a=vc<pi>())
{
    int cc=0;
    for(int i=0;i<n;i++) if(st>>i&1) cc++;
    if(n-cc<2)
    {
        upd(a);
        return;
    }
    for(int i=0;i<n;i++)
    {
        for(int j=i+1;j<n;j++)
        {
            if(!(st>>i&1)&&!(st>>j&1))
            {
                vc<pi> nxt=a;
                nxt.emplace_back(i,j);
                int nst=st|(1<<i);
                nst|=(1<<j);
                dfs(nst,nxt);
            }
        }
    }
}
int solve()
{
    ans=0;
    dfs(0);
    return ans;
}
