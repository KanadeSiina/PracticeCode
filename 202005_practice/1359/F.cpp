const double eps = 1e-9;

struct node
{
    double x,y;
    node(double x=0,double y=0):x(x),y(y) {}
    inline node operator+(const node &k1) const { return (node){k1.x + x, k1.y + y}; }
    inline node operator-(const node &k1) const { return (node){x - k1.x, y - k1.y}; }
    double abs() { return sqrt(x * x + y * y); }
    inline double dis(node k1) { return ((*this) - k1).abs(); }
    node operator*(double k1) const { return (node){x * k1, y * k1}; }
    node operator/(double k1) const { return (node){x / k1, y / k1}; }
};

typedef node Vector;
inline double cross(Vector A,Vector B){return A.x*B.y-A.y*B.x;}
inline double dot(node k1, node k2) { return k1.x * k2.x + k1.y * k2.y; }
inline double LineInter(node P,Vector v,node Q,Vector w) 
{
    Vector temp=P-Q;
    double k=cross(w,temp)/cross(v,w);
    return k;
}

struct Line
{
    node p;
    Vector v;
    Line(){}
    Line(node p,Vector v):p(p),v(v) {}
};

bool judge(Line L,node a,node b)
{
    if(cross(L.v,b)!=0)
    {
        double temp1=LineInter(L.p,L.v,a,b);
        double temp2=LineInter(a,b,L.p,L.v);
        if(temp1>=0&&temp2>=0&&temp2<=1) return true;
    }
    return false;
}
node getLL(node k1, node k2, node k3, node k4)
{
    double w1 = cross(k1 - k3, k4 - k3), w2 = cross(k4 - k3, k2 - k3);
    return (k1 * w2 + k2 * w1) / (w1 + w2);
}
inline bool same(Vector a,Vector b)
{
    return abs(cross(a,b))<eps&&dot(a,b)>0;
}
inline bool anti(Vector a,Vector b)
{
    return abs(cross(a,b))<eps&&dot(a,b)<0;
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cout<<fixed<<setprecision(10);
    int n;
    cin>>n;
    vc<Line> pt(n);
    vc<pi> dir(n);
    vc<pi> pos(n);
    vi s(n);
    for(int i=0;i<n;i++)
    {
        cin>>pos[i].first>>pos[i].second;
        cin>>dir[i].first>>dir[i].second;
        pt[i].p.x=pos[i].first;pt[i].p.y=pos[i].second;
        pt[i].v.x=dir[i].first;pt[i].v.y=dir[i].second;
        cin>>s[i];
    }
    bool suc=0;
    double ans=1e6;
    for(int i=0;i<n;i++)
    {
        for(int j=i+1;j<n;j++)
        {
            if(judge(pt[i],pt[j].p,pt[j].v))
            {
                suc=1;
                node pp=getLL(pt[i].p,pt[i].p+pt[i].v,pt[j].p,pt[j].p+pt[j].v);
                double d1=pt[i].p.dis(pp)/s[i],d2=pt[j].p.dis(pp)/s[j];
                ans=min(ans,max(d1,d2));
            }
            else{
                if(abs(cross(pt[i].v,pt[j].v))<eps)
                {
                    if(same(pt[j].p-pt[i].p,pt[j].v))
                    {
                        suc=1;
                        double d=pt[i].p.dis(pt[j].p)/s[i];
                        ans=min(ans,d);
                    }
                    if(same(pt[i].p-pt[j].p,pt[i].v))
                    {
                        suc=1;
                        double d=pt[i].p.dis(pt[j].p)/s[j];
                        ans=min(ans,d);
                    }
                    if(anti(pt[i].p-pt[j].p,pt[i].v)&&anti(pt[j].p-pt[i].p,pt[j].v))
                    {
                        suc=1;
                        ans=min(ans,pt[i].p.dis(pt[j].p)/(s[i]+s[j]));
                    }

                }
            }
        }
    }
    if(!suc) cout<<"No show :(\n";
    else cout<<ans<<'\n';

}