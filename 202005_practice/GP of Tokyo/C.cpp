const int mod=998244353;
ll quick(ll a,ll b)
{
    ll ret=1;
    while(b)
    {
        if(b&1) ret=ret*a%mod;
        a=a*a%mod;
        b>>=1;
    }
    return ret;
}
ll inv(ll a){return quick(a,mod-2);}
vi mult_mod_f(const vi&x,const vi&y,const vi&f)
{
	int n=x.size(),m=y.size();
	vi res(n+m-1);
    for(int i=0;i<n;i++)
        for(int j=0;j<m;j++)
	        res[i+j]=(res[i+j]+(ll)x[i]*y[j]%mod)%mod;
	int k=f.size();
    for(int i=n+m-2;i>=k-1;i--)
	{
		int w=(ll)res[i]*inv(f[k-1])%mod;
        for(int p=0;p<k-1;p++)
		    res[i-(k-1)+p]=(res[i-(k-1)+p]-(ll)w*f[p]%mod+mod)%mod;
	}
	res.resize(k-1);
	return res;
}
vi getf(const vi& p)
{
    int sz=p.size();
    vi f(sz+1);
    f[sz]=1;
    for(int i=0;i<sz;i++) f[sz-1-i]=mod-p[i];
    return f;
}
vi getweight(const vi&f,ll q)
{
    int n=f.size();
	vi x(n);
	x[0]=1;
	vi z{0,1};
	while(q)
    {
		if(q&1)
            x=mult_mod_f(x,z,f);
		z=mult_mod_f(z,z,f);
		q>>=1;
	}
	return x;
}
vi BM(const vi& s) {
    vi C(1,1),B(1,1);
    int L=0,m=1,b=1;
    for(int n=0;n<s.size();n++)
    {
        ll d=0;
        for(int i=0;i<L+1;i++)
            d=(d+(ll)C[i]*s[n-i])%mod;
        if (d==0) ++m;
        else if (2*L<=n) {
            vi T=C;
            ll c=mod-d*quick(b,mod-2)%mod;
            while (C.size()<B.size()+m) C.push_back(0);
            for(int i=0;i<B.size();i++)
                C[i+m]=(C[i+m]+c*B[i])%mod;
            L=n+1-L; B=T; b=d; m=1;
        } else {
            ll c=mod-d*inv(b)%mod;
            while (C.size()<B.size()+m) C.push_back(0);
            for(int i=0;i<B.size();i++)
                C[i+m]=(C[i+m]+c*B[i])%mod;
            ++m;
        }
    }
    C.erase(C.begin());
    for(int i=0;i<C.size();i++) C[i]=(mod-C[i])%mod;
    return C;
}
int gao(const vi&a,ll n)
{
	vi c=BM(a);//求解递推
	vi f=getf(c);//构造f
	vi s=getweight(f,n);//求解系数
	ll ret=0;
	for(int i=0;i<s.size();i++) ret=(ret+(ll)s[i]*a[i]%mod)%mod;
	return ret;
}
