inline bool gt(pi a,pi b)
{
    return a.first*b.second>b.first*a.second;
}
inline void upd(pi &a,pi &b)
{
    if(a.first==-1||gt(a,b)) a=b;
}
inline void mt(pi &a)
{
    ll g=__gcd(a.first,a.second);
    a.first/=g;
    a.second/=g;
    if(a.second<0) swap(a.first,a.second);
}
pi add(pi a, pi b)
{
    pi ret;
    ret.second=a.second*b.second;
    ret.first=a.first*b.second+a.second*b.first;
    mt(ret);
    return ret;
}
const int var=10000;
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int T;
    cin>>T;
    while(T--)
    {
        int atk,def,ATK,DEF;
        cin>>atk>>def>>ATK>>DEF;
        int n,m,a,b;
        cin>>n>>m>>a>>b;
        pi ans={-1,-1};int mx=(m+a-1)/a;
        int per=mx+atk-DEF;
        if(per>0)
        {
            for(int rd=max(0,n/per-1);rd<=var;rd++)
            {
                int cc=rd+1;
                pi x={DEF-atk,1};
                pi ca{n,cc};
                ca=add(ca,x);
                //dbg(ca.first,ca.second);
                if(ca.first<0) ca={0,1};
                pi tmp=ca;
                tmp.first*=a;
                mt(tmp);
                if(gt(tmp,make_pair(m,1))) continue;
                tmp.first*=-1;
                tmp=add(tmp,make_pair(m,1));
                tmp.second*=b;
                mt(tmp);
                pi cb=tmp;
                cb.first*=-1;
                pi y={ATK-def,1};
                cb=add(y,cb);
                if(cb.first<0) cb=make_pair(0,1);
                cb.first*=rd;
                //dbg(rd);
                //dbg(cb.first,cb.second);
                mt(cb);
                upd(ans,cb);
            }
        }
        if(ans.first==-1) print(-1);
        else cout<<ans.first<<'/'<<ans.second<<'\n';
    }
}