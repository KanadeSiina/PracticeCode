const int maxn=3e5+7;
int dx[]={0,1,0,-1},dy[]={1,0,-1,0};
int main()
{
    int n;
    cin>>n;
    vi p(n*n);
    for(int i=0;i<n*n;i++) cin>>p[i];
    int ans=0;
    vc<vi> d(n,vi(n));
    auto getid=[&](int r,int c){
        return r*n+c;
    };
    for(int i=0;i<n*n;i++)
    {
        int r=i/n,c=i%n;
        d[r][c]=min({r,n-1-r,c,n-1-c});
    }
    vc<vi> vis(n,vi(n));
    vc<vi> inq(n,vi(n));
    for(auto u:p)
    {
        int r=(u-1)/n,c=(u-1)%n;
        ans+=d[r][c];
        vis[r][c]=1;
        queue<pi> q;
        q.emplace(r,c);
        while(!q.empty())
        {
            auto now=q.front();q.pop();
            int x,y;
            tie(x,y)=now;
            inq[x][y]=0;
            for(int i=0;i<4;i++)
            {
                int nx=x+dx[i],ny=y+dy[i];
                if(nx<0||ny<0||nx>=n||ny>=n)
                    continue;
                if(d[nx][ny]>d[x][y]+!vis[x][y])
                {
                    d[nx][ny]=d[x][y]+!vis[x][y];
                    if(!inq[nx][ny])
                    {
                        inq[nx][ny]=1;
                        q.emplace(nx,ny);
                    }
                }
            }
        }
        /*for(int i=0;i<n;i++)
        {
            for(int j=0;j<n;j++)
                cout<<d[i][j]<<" ";
            cout<<endl;
        }*/
    }
    print(ans);
}