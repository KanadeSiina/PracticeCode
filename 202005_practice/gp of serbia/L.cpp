ll f[19][10][10];
ll g[19][10][10];
void init()
{
    for(int k=0;k<10;k++)
    {
        for(int r=9;r>=0;r--)
        {
            if(r+max(r,k)>=10)
            {
                f[1][r][k]=1;
                g[1][r][k]=max(r,k);
            }
            else{
                f[1][r][k]=f[1][r+max(r,k)][k]+1;
                g[1][r][k]=g[1][r+max(r,k)][k]+max(r,k);
            }
        }
    }
    for(int i=2;i<19;i++)
    {
        for(int k=0;k<10;k++)
        {
            for(int r=9;r>=0;r--)
            {
                int curr=r;
                for(int t=0;t<10;t++)
                {
                    f[i][r][k]+=f[i-1][curr][max(k,t)];
                    g[i][r][k]+=g[i-1][curr][max(k,t)];
                    curr=(curr+g[i-1][curr][max(k,t)])%10;
                }
            }
        }
    }
}
int main()
{
    init();
    ll a,m,n;
    cin>>a>>m>>n;
    n--;
    ll sv=a;
    auto getp=[&](ll x){
        if(x/10==0) return 1;
        int ret=0;
        ll cur=10;
        while(x%cur<10)
        {
            ret++;
            cur*=10;
        }
        return ret;
    };
    auto getmx=[&](ll x){
        int ret=0;
        while(x)
        {
            ret=max<int>(ret,x%10);
            x/=10;
        }
        return ret;
    };
    ll cnt=0;
    auto move=[&](){
        for(int i=0;i<19;i++)
        {
            while(getp(a)==i)
            {
                int rem=a%10;
                ll tmp=a;
                for(int j=0;j<i;j++) tmp/=10;
                int mx=getmx(tmp);
                if(a+g[i][rem][mx]<m) a+=g[i][rem][mx],cnt+=f[i][rem][mx];
                else break;
            }
        }
        for(int i=18;i>=0;i--)
        {
            if(i==0)
            {
                while(true)
                {
                    int mx=getmx(a);
                    if(a+mx<m) a+=mx,cnt++;
                    else break;
                }
            }
            else{
                while(true)
                {
                    int rem=a%10;
                    ll tmp=a;
                    for(int j=0;j<i;j++) tmp/=10;
                    int mx=getmx(tmp);
                    if(a+g[i][rem][mx]<m) a+=g[i][rem][mx],cnt+=f[i][rem][mx];
                    else break;
                }
            }
        }
        a+=getmx(a);
        a%=m;
        cnt++;
    };
    //dbg(cnt,a);
    vc<ll> vis(10);
    ll tarcnt=-1,beg=-1;
    for(;;)
    {
        move();
        if(a==0)
        {
            beg=cnt;
            tarcnt=1;
            break;
        }
        if(vis[a])
        {
            tarcnt=cnt-vis[a];
            beg=vis[a];
            break;
        }
        vis[a]=cnt;
    }
    //dbg(beg,tarcnt);
    if(n<=beg)
        a=sv;
    else{
        n-=beg;
        n%=tarcnt;
    }
    auto solve=[&](){
        if(a==0) return;
        for(int i=1;i<19;i++)
        {
            while(getp(a)==i)
            {
                int rem=a%10;
                ll tmp=a;
                for(int j=0;j<i;j++) tmp/=10;
                int mx=getmx(tmp);
                if(a+g[i][rem][mx]<m&&n>=f[i][rem][mx]) a+=g[i][rem][mx],n-=f[i][rem][mx];
                else break;
                if(!n) return;
            }
        }
        for(int i=18;i>=0;i--)
        {
            if(i==0)
            {
                while(true)
                {
                    int mx=getmx(a);
                    if(a+mx<m) a+=mx,n--;
                    else break;
                    if(!n) return;
                }
            }
            else{
                while(true)
                {
                    int rem=a%10;
                    ll tmp=a;
                    for(int j=0;j<i;j++) tmp/=10;
                    int mx=getmx(tmp);
                    if(a+g[i][rem][mx]<m&&n>=f[i][rem][mx]) a+=g[i][rem][mx],n-=f[i][rem][mx];
                    else break;
                    if(!n) return;
                }
            }
        }
        if(!n) return;
        a+=getmx(a);
        a%=m;
        n--;
    };
    while(n)
    {
        solve();
        if(!a) break;
    }
    print(a);
}