#define lson o<<1
#define rson o<<1|1
#define Lson L,mid,lson
#define Rson mid+1,R,rson
struct D{
    int mx;
    D(ll v=-2e9):mx(v){}
    inline void add(int v)
    {
        mx=max(mx,v);
    }
    static D merge(D &x,D &y)
    {
        return D(max(x.mx,y.mx));
    }
};
int sz;
template<class D>
struct sgt{
    vc<D> tree;
    sgt(){}
    template<class T>
    sgt(const vc<T>& a)
    {
        int n=a.size();
        sz=1;
        while(sz<n) sz*=2;
        tree.resize(sz*2);
        for(int i=0;i<n;i++) tree[sz+i]=D(a[i]);
        for(int i=sz-1;i>=1;i--) upd(i);
    }
    inline void upd(int o)
    {
        tree[o]=D::merge(tree[lson],tree[rson]);
    }
    void add(int l,int r,int val,int L=0,int R=sz-1,int o=1)
    {
        if(l<=L&&r>=R)
        {
            tree[o].add(val);
            return;
        }
        int mid=L+R>>1;
        if(l<=mid) add(l,r,val,Lson);
        if(r>mid) add(l,r,val,Rson);
        upd(o);
    }
    int query(int l,int r,int L=0,int R=sz-1,int o=1)
    {
        if(l<=L&&r>=R)
            return tree[o].mx;
        int mid=L+R>>1;
        int ret=-2e9;
        if(l<=mid) ret=max(ret,query(l,r,Lson));
        if(r>mid) ret=max(ret,query(l,r,Rson));
        return ret;
    }
};
struct P{
    int x,y,z;
    bool operator<(const P&a) const{
        if(x==a.x)
        {
            if(y==a.y) return z<a.z;
            return y<a.y;
        }
        return x<a.x;
    } 
};
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n,a,b;
    cin>>n>>a>>b;
    vc<P> pt(n);
    vi all;
    for(int i=0;i<n;i++)
    {
        cin>>pt[i].x>>pt[i].y>>pt[i].z;
        all.push_back(pt[i].y);
    }
    mkuni(all);
    for(int i=0;i<n;i++)
        pt[i].y=lower_bound(all.begin(),all.end(),pt[i].y)-all.begin();
    auto check=[&](int x){
        vi cc(2);
        for(int i=0;i<x;i++) cc[(pt[i].x+all[pt[i].y]+pt[i].z)&1]++;
        if(cc[0]&&cc[1]) return false;
        vc<P> cur=vc<P>(pt.begin(),pt.begin()+x);
        sort(cur.begin(),cur.end());
        int sz=all.size()-1;
        sgt<D> up1(vi(sz+1,-2e9));
        sgt<D> up2=up1,down1=up1,down2=up1;
        for(auto &u:cur)
        {
            int x=u.x,y=all[u.y],z=u.z;
            //dbg(x,y,z);
            if(x+y-z<up1.query(0,u.y)) return false;
            if(x+y+z<up2.query(0,u.y)) return false;
            if(x-y-z<down1.query(u.y,sz)) return false;
            if(x-y+z<down2.query(u.y,sz)) return false;
            up1.add(u.y,u.y,x+y-z);
            up2.add(u.y,u.y,x+y+z);
            down1.add(u.y,u.y,x-y-z);
            down2.add(u.y,u.y,x-y+z);
        }
        return true;
    };
    int L=1,R=n,ans=-1;
    while(L<=R)
    {
        int mid=L+R>>1;
        if(check(mid))
        {
            L=mid+1;
            ans=mid;
        }
        else R=mid-1;
    }
    if(ans==n) cout<<"bravo komisijo\n";
    else print(ans+1);
}