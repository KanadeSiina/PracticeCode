struct P{
    int x,y,z;
    bool operator<(const P&a) const{
        if(x==a.x)
        {
            if(y==a.y) return z<a.z;
            return y<a.y;
        }
        return x<a.x;
    } 
};
bool solve(vc<P>& cur)
{
    int sz=cur.size();
    if(sz==1) return true;
    int mid=sz/2;
    vc<P> l,r;
    for(int i=0;i<mid;i++) l.push_back(cur[i]);
    for(int i=mid;i<sz;i++) r.push_back(cur[i]);
    bool ret=1;
    ret&=solve(l);
    ret&=solve(r);
    int lp=0,rp=0;
    int mxj1=-1e9,mxj2=-1e9,mii1=2e9,mii2=2e9;
    vc<P> nxt;
    while(lp<l.size()&&rp<r.size())
    {
        P c;
        if(l[lp].y<r[rp].y)
        {
            c=l[lp];
            if(c.x-c.y-c.z>mii1) ret=0;
            if(c.x-c.y+c.z>mii2) ret=0;
            mxj1=max(mxj1,c.x+c.y-c.z);
            mxj2=max(mxj2,c.x+c.y+c.z);
            lp++;
        }
        else{
            c=r[rp];
            if(c.x+c.y-c.z<mxj1) ret=0;
            if(c.x+c.y+c.z<mxj2) ret=0;
            mii1=min(mii1,c.x-c.y-c.z);
            mii2=min(mii2,c.x-c.y+c.z);
            rp++;
        }
        nxt.push_back(c);
    }
    while(lp<l.size())
    {
        P c=l[lp];
        if(c.x-c.y-c.z>mii1) ret=0;
        if(c.x-c.y+c.z>mii2) ret=0;
        mxj1=max(mxj1,c.x+c.y-c.z);
        mxj2=max(mxj2,c.x+c.y+c.z);
        lp++;
        nxt.push_back(c);
    }
    while(rp<r.size())
    {
        P c=r[rp];
        if(c.x+c.y-c.z<mxj1) ret=0;
        if(c.x+c.y+c.z<mxj2) ret=0;
        mii1=min(mii1,c.x-c.y-c.z);
        mii2=min(mii2,c.x-c.y+c.z);
        rp++;
        nxt.push_back(c);
    }
    cur=nxt;
    return ret;
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n,a,b;
    cin>>n>>a>>b;
    vc<P> pt(n);
    for(int i=0;i<n;i++) cin>>pt[i].x>>pt[i].y>>pt[i].z;
    auto check=[&](int x)
    {
        vi cc(2);
        for(int i=0;i<x;i++) cc[(pt[i].x+pt[i].y+pt[i].z)&1]++;
        if(cc[0]&&cc[1]) return false;
        vc<P> cur=vc<P>(pt.begin(),pt.begin()+x);
        sort(cur.begin(),cur.end());
        return solve(cur);
    };
    int L=1,R=n,ans=-1;
    while(L<=R)
    {
        int mid=L+R>>1;
        if(check(mid))
        {
            L=mid+1;
            ans=mid;
        }
        else R=mid-1;
    }
    if(ans==n) cout<<"bravo komisijo\n";
    else print(ans+1);
}