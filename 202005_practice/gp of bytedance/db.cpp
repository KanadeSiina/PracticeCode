map<string,int> id;
int cid;
inline int getid(string s)
{
    if(id.count(s)) return id[s];
    id[s]=cid;
    return id[s];
}
const int var=9;
map<string,int> isroot;
void dfs(string s)
{
    if(s.length()>=13) return;
    if(id.count(s)) return;
    getid(s);
    dfs("012"+s);
    dfs("22"+s);
    for(int i=0;i<s.length();i++)
    {
        dfs(s.substr(0,i+1)+"012"+s.substr(i+1,s.length()-1-i));
        dfs(s.substr(0,i+1)+"012"+s.substr(i+1,s.length()-1-i));
        dfs(s.substr(0,i+1)+"22"+s.substr(i+1,s.length()-1-i));
        dfs(s.substr(0,i+1)+"22"+s.substr(i+1,s.length()-1-i));
    }
    for(int i=1;i<s.length();i++)
    {
        string nxt;
        if(s[i]=='0'&&s[i-1]=='0')
        {
            nxt=s;
            nxt[i]='2';
            nxt[i-1]='1';
            dfs(nxt);
        }
        if(s[i]=='2'&&s[i-1]=='1')
        {
            nxt=s;
            nxt[i]=nxt[i-1]='0';
            dfs(nxt);
        }
        if(s[i]=='0'&&s[i-1]=='2')
        {
            nxt="";
            for(int j=0;j<i-1;j++) nxt+=s[j];
            nxt+="111";
            nxt+=s.substr(i+1,s.length()-1-i);
            dfs(nxt);
        }
        if(i>=2&&s[i-2]=='1'&&s[i]=='1'&&s[i-1]=='1')
        {
            nxt="";
            for(int j=0;j<i-2;j++) nxt+=s[j];
            nxt+="20";
            nxt+=s.substr(i+1,s.length()-i-1);
            dfs(nxt);
        }
        if(s[i]=='2'&&s[i-1]=='2')
            dfs(s.substr(0,i-1)+s.substr(i+1,s.length()-i-1));
        if(i>=2&&s[i]=='2'&&s[i-1]=='1'&&s[i-2]=='0')
            dfs(s.substr(0,i-2)+s.substr(i+1,s.length()-i-1));
    }
}
