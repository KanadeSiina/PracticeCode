int main()
{
    int ans=0;
    for(int len=0;len<var;len++)
    {
        int t=1;
        for(int i=0;i<len;i++) t*=3;
        for(int i=0;i<t;i++)
        {
            string cur;
            int cc=i;
            for(int j=0;j<len;j++)
            {
                cur+='0'+cc%3;
                cc/=3;
            }
            if(!id.count(cur))
            {
                cout<<cur<<'\n';
                cid=ans;
                isroot[cur]=ans;
                ans++;
                dfs(cur);
            }
        }
    }
    vc<vi> G(25,vi(3,-1));
    for(auto u:isroot)
    {
        string s=u.first;
        int idc=u.second;
        for(auto v:isroot)
        {
            int tid=v.second;
            for(int j=0;j<3;j++)
            {
                string cur=s+char(j+'0');
                if(id[cur]==tid)
                {
                    assert(G[idc][j]==-1);
                    G[idc][j]=tid;
                }
            }
        }
    }
    for(int i=0;i<ans;i++) dbg(i,G[i]);
    for(int i=0;i<ans;i++)
    {
        cout<<'{';
        for(int j=0;j<3;j++) cout<<G[i][j]<<",}"[j==2];
        cout<<',';
    }
    cout<<ans<<'\n';
}