#define lson o<<1
#define rson o<<1|1
#define Lson L,mid,lson
#define Rson mid+1,R,rson
int n;
const int maxn=4e5+7;
int mx[maxn<<2],lz[maxn<<2];
void build(int L=0,int R=n*2-1,int o=1)
{
    if(L==R)
    {
        mx[o]=L-n*2;
        return ;
    }
    int mid=L+R>>1;
    build(Lson);
    build(Rson);
    mx[o]=max(mx[lson],mx[rson]);
}
inline void pushdown(int o)
{
    if(lz[o])
    {
        lz[lson]+=lz[o];
        lz[rson]+=lz[o];
        mx[lson]+=lz[o];
        mx[rson]+=lz[o];
        lz[o]=0;
    }
}
void add(int l,int r,int v,int L=0,int R=n*2-1,int o=1)
{
    if(l<=L&&r>=R)
    {
        mx[o]+=v;
        lz[o]+=v;
        return;
    }
    pushdown(o);
    int mid=L+R>>1;
    if(l<=mid) add(l,r,v,Lson);
    if(r>mid) add(l,r,v,Rson);
    mx[o]=max(mx[lson],mx[rson]);
}
int query(int l,int r,int L=0,int R=n*2-1,int o=1)
{
    if(l<=L&&r>=R) return mx[o];
    pushdown(o);
    int mid=L+R>>1;
    int ret=-1e9;
    if(l<=mid) ret=max(ret,query(l,r,Lson));
    if(r>mid) ret=max(ret,query(l,r,Rson));
    return ret;
}
