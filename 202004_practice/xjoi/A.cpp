const int maxn=50;
vc<pi> G[maxn][maxn];
bool vis[maxn][maxn];
int dx[]={1,1,-1,-1},dy[]={1,-1,-1,1};
int n;
int cnt=0;
vc<pi> ans;
void dfs(int x,int y)
{
    if(vis[x][y]) return;
    vis[x][y]=1;
    ans.emplace_back(x,y);
    cnt++;
    if(cnt==n*(n+1))
    {
        for(auto u:ans) cout<<u.first<<" "<<u.second<<endl;
        exit(0);
    }
    for(auto v:G[x][y])
        dfs(v.first,v.second);
    vis[x][y]=0;
    cnt--;
    ans.pop_back();
}
int main()
{
    cin>>n;
    int r=n,c=n+1;
    for(int i=1;i<=r;i++)
    {
        for(int j=1;j<=c;j++)
        {
            for(int k=0;k<4;k++)
            {
                int nx=i+dx[k],ny=j+dy[k];
                if(nx<=0||ny<=0||nx>r||ny>c) continue;
                //dbg(i,j,nx,ny);
                G[i][j].emplace_back(nx,ny);
            }
        }
        G[1][i].emplace_back(1,i+1);
        G[r][i+1].emplace_back(r,i);
        if(i>1)
        {
            G[i][1].emplace_back(i-1,1);
            G[i-1][c].emplace_back(i,c);
        }
    }
    cout<<n*(n+1)-1<<endl;
    dfs(1,1);

}