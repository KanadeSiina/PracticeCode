pi getone()
{
    int x=rand_int(low,up),y=rand_int(low,up);
    while(!ask(x,y))
    {
        x=rand_int(low,up);
        y=rand_int(low,up);
    }
    return make_pair(x,y);
}
void solve()
{
    suc=0;
    auto tar=getone();
    if(suc) return;
    int lx,rx,ly,ry;
    {
        int L=low,R=tar.first;
        while(L<=R)
        {
            int mid=(L+R)/2;
            int cur=ask(mid,tar.second);
            if(cur)
            {
                R=mid-1;
                lx=mid;
            }
            else L=mid+1;
            if(suc) return;
        }
    }
    {
        int L=tar.first,R=up;
        while(L<=R)
        {
            int mid=(L+R)/2;
            int cur=ask(mid,tar.second);
            if(cur)
            {
                L=mid+1;
                rx=mid;
            }
            else R=mid-1;
            if(suc) return;
        }
    }
    {
        int L=low,R=tar.second;
        while(L<=R)
        {
            int mid=(L+R)/2;
            int cur=ask(tar.first,mid);
            if(cur)
            {
                R=mid-1;
                ly=mid;
            }
            else L=mid+1;
            if(suc) return;
        }
    }
    {
        int L=tar.second,R=up;
        while(L<=R)
        {
            int mid=(L+R)/2;
            int cur=ask(tar.first,mid);
            if(cur)
            {
                L=mid+1;
                ry=mid;
            }
            else R=mid-1;
            if(suc) return;
        }
    }
    int ret=ask((lx+rx)/2,(ly+ry)/2);
    assert(ret==2);
    return;
}
