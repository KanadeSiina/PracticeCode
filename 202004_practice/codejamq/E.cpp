const int maxn=100;
const int INF = 0x3f3f3f3f;
int dis;
int cx[maxn], cy[maxn];
int dx[maxn], dy[maxn];
int a[maxn][maxn];
bool vis[maxn];

int n,k;
bool bfs_findPath() {
    queue<int> q;
    memset(dx, -1, sizeof(dx));
    memset(dy, -1, sizeof(dy));
    // 使用BFS遍历对图的点进行分层，从X中找出一个未匹配点v
    // (所有v)组成第一层，接下来的层都是这样形成——每次查找
    // 匹配点(增广路性质)，直到在Y中找到未匹配点才停止查找，
    // 对X其他未匹配点同样进行查找增广路径(BFS只分层不标记
    // 是否匹配点)
    // 找出X中的所有未匹配点组成BFS的第一层
    dis = INF;
    for(int i = 0; i < n; ++i) {
        if(cx[i] == -1) {
            q.push(i);
            dx[i] = 0;
        }
    }
    while(!q.empty()) {
        int u = q.front();
        q.pop();
        if(dx[u] > dis) break;// 该路径长度大于dis，等待下一次BFS扩充
        for(int v = 0; v < n; ++v) {
            if(a[u][v] && dy[v] == -1) {// (u,v)之间有边且v还没有分层
                dy[v] = dx[u] + 1;
                if(cy[v] == -1) dis = dy[v];// v是未匹配点，停止延伸（查找）,得到本次BFS的最大遍历层次
                else {// v是已匹配点，继续延伸
                    dx[cy[v]] = dy[v] + 1;
                    q.push(cy[v]);
                }
            }
        }
    }
    return dis != INF;// 若dis为INF说明Y中没有未匹配点，也就是没有增广路径了
}

bool dfs(int u) {
    for(int v = 0; v < n; ++v) {
        if(!vis[v] && a[u][v] && dy[v] == dx[u] + 1) {
            vis[v] = 1;
            if(cy[v] != -1 && dy[v] == dis) continue;
            if(cy[v] == -1 || dfs(cy[v])) {// 是增广路径，更新匹配集
                cy[v] = u;
                cx[u] = v;
                return true;
            }
        }
    }
    return false;
}

int HK() {
    int ans = 0;
    memset(cx, -1, sizeof(cx));
    memset(cy, -1, sizeof(cy));
    while(bfs_findPath()) {// 有增广路
        memset(vis, 0, sizeof(vis));
        for(int i = 0; i < n; ++i) {
            // 用DFS查找增广路径，增广路径一定从未匹配点开始
            // 如果查找到一个增广路径，匹配数加一
            if(cx[i] == -1 && dfs(i)) ++ans;
        }
    }
    return ans;
}
vc<vi> maze;
bool getcl(int u,int sum=0)
{
    if(u==n)
    {
        if(sum!=k) return false;
        vi cnt(n+1);
        for(int i=0;i<u;i++) cnt[maze[i][i]]++;
        for(int i=1;i<=n;i++) if(cnt[i]==n-1)
            return false;
        return true;
    }
    if(sum+n-u>k) return false;
    if(sum+(n-u)*n<k) return false;
    for(int i=1;i<=n&&sum+i<=k;i++)
    {
        maze[u][u]=i;
        if(getcl(u+1,sum+i)) return true;
    }
    return false;
}
bool solve()
{
    maze=vc<vi>(n,vi(n));
    if(!getcl(0)) return false;
    vi cnt(n+1);
    vi id(n);
    for(int i=0;i<n;i++) id[i]=i+1;
    for(int i=0;i<n;i++) cnt[maze[i][i]]++;
    sort(id.begin(),id.end(),[&](int i,int j){return cnt[i]>cnt[j];});
    auto check=[&](int i,int j,int x){
        for(int r=0;r<n;r++)
        {
            if(maze[r][j]==x||maze[i][r]==x)
                return false;
        }
        return true;
    };
    auto makex=[&](int i)
    {
        memset(a,0,sizeof(a));
        for(int r=0;r<n;r++)
        {
            for(int c=0;c<n;c++)
            {
                if(!maze[r][c]&&check(r,c,i))
                    a[r][c]=1;
            }
        }
        HK();
        for(int r=0;r<n;r++)
        {
            if(cx[r]!=-1)
                maze[r][cx[r]]=i;
        }
        int tot=0;
        for(int r=0;r<n;r++)
            for(int c=0;c<n;c++)
            {
                tot+=maze[r][c]==i;
                //cout<<maze[r][c]<<" \n"[c==n-1];
            }
        assert(tot==n);
    };
    for(int i=0;i<n;i++)
        makex(id[i]);
    return true;
}
