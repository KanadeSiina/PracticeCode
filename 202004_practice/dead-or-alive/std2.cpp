const int maxn = 6e5 + 7;
vi G[maxn];
int ti; //时间戳
int ts[maxn];
int depth[maxn];
int far[maxn][22];
int dfn[maxn];
void dfs(int u, int fa = -1)
{
    dfn[u] = ++ti; //dfs序
    ts[ti] = u;    //括号序列时间戳映射
    for (auto v:G[u])
    {
        if (v == fa)
            continue;
        depth[v] = depth[u] + 1;
        far[v][0] = u; //可能会用到的倍增数组
        dfs(v, u);
        ts[++ti] = u;
    }
}
int ST[maxn][20]; //LCA转RMQ用ST表求
bool cmp(int x, int y) { return depth[x] < depth[y]; }
void RMQ()
{
    for (int i = 1; i <= ti; i++)
        ST[i][0] = ts[i];
    for (int j = 1; (1 << j) <= ti; j++)
    {
        for (int i = 1; i + (1 << (j - 1)) - 1 <= ti; i++)
        {
            if (cmp(ST[i][j - 1], ST[i + (1 << (j - 1))][j - 1]))
                ST[i][j] = ST[i][j - 1];
            else
                ST[i][j] = ST[i + (1 << (j - 1))][j - 1];
        }
    }
}
inline int LCA(int u, int v)
{
    int l = dfn[u], r = dfn[v];
    if (l > r)
        swap(l, r);
    int k = 31 - __builtin_clz(r - l + 1);
    if (cmp(ST[l][k], ST[r - (1 << k) + 1][k]))
        return ST[l][k];
    return ST[r - (1 << k) + 1][k];
}
set<int> used;
vi vg[maxn];
inline void access(int u,int v)
{
    used.insert(u);
    used.insert(v);
    vg[u].push_back(v);
    vg[v].push_back(u);
}
void Virtual(vector<int> &all) //关键点
{
    static int st[maxn]; //栈模拟访问
    int top = 0;
    sort(all.begin(),all.end(),[&](int i,int j){return dfn[i]<dfn[j];});
    for (auto &u : all)
    {
        if (top == 0)
            st[++top] = u;
        else
        {
            int lca = LCA(st[top], u);
            while (top > 1 && dfn[st[top - 1]] >= dfn[lca])
            { //栈中至少有两个元素，则开始向上连边
                access(st[top],st[top-1]);
                top--;
            }
            if (lca != st[top]) //最后将lca也放进去
            {
                access(st[top], lca);
                st[top] = lca;
            }
            st[++top] = u;
        }
    }
    while (top > 1) //所有元素出栈
    {
        access(st[top], st[top - 1]);
        --top;
    }
}
ll dp[maxn];
ll solve(ll u,int fa=-1)
{
    ll ret=dp[u];
    for(auto v:vg[u])if(v!=fa)
    {
        ll cur=solve(v,u);
        //dbg(u,v,cur,cur-(depth[v]-depth[u]-1));
        if(cur)
            ret+=max(1LL,cur-(depth[v]-depth[u]-1));
    }
    //dbg(u,ret);
    return ret>1?ret-1:ret;
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n,rt;
    cin>>n>>rt;
    vi a(n+1);
    for(int i=1;i<=n;i++) cin>>a[i];
    for(int i=1,u,v;i<n;i++)
    {
        cin>>u>>v;
        G[u].push_back(v);
        G[v].push_back(u);
    }
    dfs(rt);
    RMQ();
    vc<vi> d(n);
    for(int i=1;i<=n;i++)
    {
        if(i!=rt)
            d[depth[i]].push_back(i);
    }
    ll ans=0;
    for(int i=0;i<n;i++)
    {
        if(i&&!d[i].size())break;
        d[i].push_back(rt);
        for(auto u:d[i])
        {
            if(!i||u!=rt)
                dp[u]=a[u];
        }
        Virtual(d[i]);
        ans+=solve(rt);
        dp[rt]=0;
        for(auto u:used) vg[u].clear(),dp[u]=0;
        used.clear();
        //dbg(i,ans);
    }
    print(ans);
}